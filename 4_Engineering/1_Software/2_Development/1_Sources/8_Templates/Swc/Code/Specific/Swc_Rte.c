/**
 * \brief      Reads the input control values from the RTE through the configured read APIs.
 * \param      -
 * \return     -
 */
void Swc_gv_RteInputUpdate(void)
{
   Rte_Read_SwcIn_First(&Swc_gauc_InputValues[SWC_FIRST_INSTANCE]);
   Rte_Read_SwcIn_Second(&Swc_gauc_InputValues[SWC_SECOND_INSTANCE]);
}

/**
 * \brief      Writes the processed output values to RTE through the configured write APIs.
 * \param      -
 * \return     -
 */
void Swc_gv_RteOutputUpdate(void)
{
   Rte_Write_SwcOut_First(&Swc_gauc_OutputValues[SWC_FIRST_INSTANCE]);
   Rte_Write_SwcOut_Second(&Swc_gauc_OutputValues[SWC_SECOND_INSTANCE]);
}
